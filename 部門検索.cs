		private void 頁情報設定()
		{
			axGT部門.Clear();

// MOD 2005.05.12 東都）小童谷 一行目空白 START
//			i開始数 = (i現在頁数 - 1) * axGT部門.Rows + 1;
			i開始数 = (i現在頁数 - 1) * (axGT部門.Rows - 1) + 1;
//			i終了数 = i現在頁数 * axGT部門.Rows;
			i終了数 = i現在頁数 * (axGT部門.Rows - 1);

//			short s表示数 = (short)1;
			short s表示数 = (short)2;
// MOD 2005.05.12 東都）小童谷 一行目空白 END
			for(short sCnt = (short)i開始数; sCnt < s部門一覧.Length && sCnt <= i終了数 ; sCnt++)
			{
				axGT部門.set_RowsText(s表示数, s部門一覧[sCnt]);
				s表示数++;
			}
			lab頁番号.Text = i現在頁数.ToString() + " / " + i最大頁数.ToString();
			if (i現在頁数 == 1)
				btn前頁.Enabled = false;
			else
				btn前頁.Enabled = true;
			if (i現在頁数 == i最大頁数)
				btn次頁.Enabled = false;
			else
				btn次頁.Enabled = true;
			axGT部門.Focus();
		}

