			tex部門名.Text = tex部門名.Text.Trim();
			if(!半角チェック(tex部門コード, "コード")) return;

			axGT部門.Clear();

			texメッセージ.Text = "検索中．．．";

			s部門一覧 = new string[1];
			// カーソルを砂時計にする
			Cursor = System.Windows.Forms.Cursors.AppStarting;
			try
			{
				string[] sKey  = new string[3];
				sKey[0] = s会員コード;
				sKey[1] = tex部門コード.Text;
				sKey[2] = tex部門名.Text;
				s部門一覧 = sv_maintenance.Get_Section(gsaユーザ, sKey);
			}
// ADD 2005.05.25 東都）小童谷 通信エラーのメッセージ修正 START
			catch (System.Net.WebException)
			{
				s部門一覧[0] = gs通信エラー;
			}
// ADD 2005.05.25 東都）小童谷 通信エラーのメッセージ修正 END
			catch (Exception ex)
			{
				s部門一覧[0] = "通信エラー：" + ex.Message;
			}
				
			// カーソルをデフォルトに戻す
			Cursor = System.Windows.Forms.Cursors.Default;

			if (s部門一覧[0].Equals("正常終了"))
			{
				texメッセージ.Text ="";
// MOD 2005.05.12 東都）小童谷 一行目空白 START
//				i最大頁数 = (s部門一覧.Length - 2) / axGT部門.Rows + 1;
				i最大頁数 = (s部門一覧.Length - 2) / (axGT部門.Rows - 1) + 1;
// MOD 2005.05.12 東都）小童谷 一行目空白 END
				if (i現在頁数 > i最大頁数)
					i現在頁数 = i最大頁数;
				頁情報設定();

// ADD 2005.05.12 東都）小童谷 一行目選択 START
				axGT部門.CaretRow = 1;
				axGT部門_CurPlaceChanged(null,null);
// ADD 2005.05.12 東都）小童谷 一行目選択 END

				axGT部門.Focus();
			}
			else
			{
				if (s部門一覧[0].Equals("該当データがありません"))
				{
					texメッセージ.Text = "";
					MessageBox.Show("該当データがありません","部門検索",MessageBoxButtons.OK);
				}
				else
				{
					texメッセージ.Text = s部門一覧[0];
					i現在頁数 = 1;
					btn前頁.Enabled = false;
					btn次頁.Enabled = false;
					lab頁番号.Text = "";
					ビープ音();
				}
				tex部門コード.Focus();
			}
		}

